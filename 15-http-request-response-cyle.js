// HTTP Request / Response Cycle

// Everytime we open up a browser and type in a URL, we are making a request to a server
// The server then responds with some data
// The browser then renders that data for us to see
// We can use NodeJS to create a server that responds to requests

// HTTP structure
// Request Messages are the users sending a request to the server. HTTP Request Messages contain a request line, header lines, and an optional message body.
// Response Messages are the servers sending a response to the user. HTTP Response Messages contain a status line, header lines, and an optional message body.

// GET request - Read Data
// POST request - Insert Data
// PUT request - Update Data
// DELETE request - Delete Data

// HTTP Status Codes
// 1xx - Informational
// 2xx - Success
// 3xx - Redirection
// 4xx - Client Error
// 5xx - Server Error
